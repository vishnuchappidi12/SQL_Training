------------------------JOINS IN SQL----------------------
1.Cartesian Product : Every record in a tbale will join with every record in another table.
2.Inner join: Every record in a table join with matching records in other table, Based on common Feild.  
3.Left join: common records from the both the tables, additional records from left side table.
4.Right Join: Common records from both the tables,remain records from right side table.
5.Full Join: Common records from both the tables, Left out records from the both the tables.
6.Conditional join: Using Some particular condidtion. 
7.Self join: If table join itself is called self join.
8.Date operations: performing a dates.

